function get_user_country($country = null)
{
    if (@GEOS[$country]) {
        return $country;
    }
    if (@$_SERVER['GEOIP2_COUNTRY_ISO_CODE']) {
        return @$_SERVER['GEOIP2_COUNTRY_ISO_CODE'];
    }

    $ip = tiny::getClientRealIP();
    if ($ip != '127.0.0.1') {
        $geo = tiny::http()->get('https://api.ip2country.info/ip?' . $ip)->json;
        if ($geo) {
            return $geo->countryCode;
        }
    }
    return 'A1';
}

function format_phone_number($number, $country)
{

    if (!isset(GEOS[$country])) {
        return $number;
    }

    if (substr($number, 0, 1) == '+') {
        return substr($number, 1);
    }

    if (substr($number, 0, 1) == '0') {
        $number = substr($number, 1);
    }

    // starts with country code?
    foreach (GEOS as $geo => $data) {
        if ($geo == $country) {
            $prefix = $data['phone'][0];
            $prefix_len = strlen($prefix);
            if (substr($number, 0, $prefix_len) == $prefix) {
                return $number;
            }
            return $prefix . $number;
        }
    }

    return $number;
}

