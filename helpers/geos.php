function currency_by_region($region): string
{
    if (in_array($region, CURRENCIES['GBP']['countries'])) {
        return 'GBP';
    }
    if (in_array($region, CURRENCIES['EUR']['countries'])) {
        return 'EUR';
    }
    if (in_array($region, CURRENCIES['USD']['countries'])) {
        return 'USD';
    }
    foreach (CURRENCIES as $code => $item) {
        if (in_array($code, SUPPORTED_CURRENCIES)) {
            if (in_array($region, $item['countries'])) {
                return $code;
            }
        }
    }
    return 'USD';
}

